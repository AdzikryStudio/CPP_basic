#include <iostream>

using namespace std;

int main(){
	
	//operator assignment
	//variabel = eksperesi
	// +, -, /, *, %
	
	//assignment
	int a = 10;
	cout <<"nilai awal dari a adalah: " << a << endl;
	
	//plus
	 a += 2;	
	cout << " nilai a ditambah 2 adalah: "<< a << endl;
	
	//minus
	 a -= 2;	
	cout << " nilai a dikurangi 2 adalah: "<< a << endl;
	
	//kali
	 a *= 2;	
	cout << " nilai a dikali 2 adalah: "<< a << endl;
	
	//bagi
	 a /= 2;	
	cout << " nilai a dibagi 2 adalah: "<< a << endl;
	
	//modulus
	 a %= 2;	
	cout << " nilai a dimodulus 2 adalah: "<< a << endl;
	
	return 0;
}
