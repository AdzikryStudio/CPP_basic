#include <iostream>

using namespace std;

int main(){
	
	//incerment(+) and decerment(-)
	//inccerment = preincerment and postincerment
	//decerment =
	
	int a = 5;
	int b = 12;
	//preincerment (sebelum)
	++a;
	cout << a << endl;
	
	//postincermen (sesudah)
	b++;
	cout << b << endl;
	
	//postdecerment (sesudah)
	a--;
	cout << a << endl;
	
	//predecerment (sebelum)
	--b;
	cout << b << endl;
	return 0;
	
	
}
